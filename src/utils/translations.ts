// Get translation for a key
export const t = (key: string, language: Language = 'en'): string => {
  const translation = translations[key]
  if (!translation) {
    console.warn(`Translation missing for key: ${key}`)
    return key
  }
  return translation[language] || translation.en
}

// Get direction for language (RTL for Arabic)
export const getDirection = (language: Language): 'ltr' | 'rtl' => {
  return language === 'ar' ? 'rtl' : 'ltr'
}

// Get font family for language
export const getFontFamily = (language: Language): string => {
  switch (language) {
    case 'ar':
      return "'Tajawal', 'Segoe UI', Tahoma, sans-serif"
    case 'fr':
      return "'Roboto', 'Segoe UI', sans-serif"
    default:
      return "'Inter', 'Segoe UI', sans-serif"
  }
}

// Hook-like function to get current language from store
export const getCurrentTranslations = (language: Language) => {
  return {
    t: (key: string) => translations[key]?.[language] || translations[key]?.en || key,
    dir: getDirection(language),
    font: getFontFamily(language)
  }
}

export default { translations, t, getDirection, getFontFamily, getCurrentTranslations }
